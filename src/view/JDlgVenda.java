    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed
        // TODO add your handling code here:
        if (pesquisar == false) {
            JDlgVendaPesquisar telaPesquisa = new JDlgVendaPesquisar(null, true);
            telaPesquisa.setTelaAnterior(this);
            telaPesquisa.setVisible(true);
        }
        habilitar(true);
        Util.habilitar(false, jTxtID);
        incluir = false;
        pesquisar = false;
    }//GEN-LAST:event_jBtnAlterarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        if (pesquisar == false) {
            JDlgVendaPesquisar telaPesquisa = new JDlgVendaPesquisar(null, true);
            telaPesquisa.setTelaAnterior(this);
            telaPesquisa.setVisible(true);
        }
        if (Util.perguntar("Confirme exclusão!", "Deletar registro")) {
            VccVenda venda = viewbean();
            VccVendaDAO dao = new VccVendaDAO();
            dao.delete(venda);
            Util.mostrar("Exclusão realizada");
            limpar();

        } else {
            Util.mostrar("Exclusão cancelada");
            habilitar(false);
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        VccVenda venda = viewbean();
        VccVendaDAO dao = new VccVendaDAO();
        if (incluir == true) {
            dao.insert(venda);
        } else {
            dao.update(venda);
        }
        habilitar(false);

        limpar();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

