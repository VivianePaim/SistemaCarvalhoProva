    public JDlgVenda() {
        initComponents();
        setTitle("Venda");
        setLocationRelativeTo(null);
        habilitar(false);
        limpar();
        
        
        VccClienteDAO clienteDAO = new VccClienteDAO();
        List listaClientes = clienteDAO.listAll();
        for (Object cliente : listaClientes) {
            jCboCliente.addItem((VccCliente) cliente);
        }
        VccFornecedorDAO fornecedorDAO = new VccFornecedorDAO();
        List listaFornecedores = fornecedorDAO.listAll();
        for (Object fornecedor : listaFornecedores) {
            jCboFornecedor.addItem((VccFornecedor) fornecedor);
        }
        
    }

    private void habilitar(boolean status) {
        if (status) {
            Util.habilitar(true, jTxtID, jTxtTotal, jFmtData, jCboCliente, jCboFornecedor,
                    jBtnCancelar, jBtnConfirmar, jBtnIncluirProduto, jBtnAlterarProduto, jBtnExcluirProduto);
            Util.habilitar(false, jBtnIncluir, jBtnAlterar, jBtnPesquisar, jBtnExcluir);
        } else {
            Util.habilitar(false, jTxtID, jTxtTotal, jFmtData, jCboCliente, jCboFornecedor,
                    jBtnCancelar, jBtnConfirmar, jBtnIncluirProduto, jBtnAlterarProduto, jBtnExcluirProduto);
            Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnPesquisar, jBtnExcluir);
        }

    }

    private void limpar() {
        Util.limpar(jTxtID, jTxtTotal, jFmtData, jCboCliente, jCboFornecedor);
    }

    private VccVenda viewbean() {
        VccVenda vcc_venda = new VccVenda();
        try {

            //adicionando ao bean id
            int cod = Util.strToInt(jTxtID.getText());
            vcc_venda.setIdVccVenda(cod);
            vcc_venda.setVccDataVenda(Util.strToDate(jFmtData.getText()));
            vcc_venda.setVccTotalVenda(Util.strToDouble(jTxtTotal.getText()));
            vcc_venda.setVccCliente((VccCliente) jCboCliente.getSelectedItem());
            vcc_venda.setVccFornecedor((VccFornecedor) jCboFornecedor.getSelectedItem());

        } catch (ParseException ex) {
            Logger.getLogger(JDlgVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        return vcc_venda;
    }

    void beanview(VccVenda vcc_venda) {
        jTxtID.setText(Util.intToStr(vcc_venda.getIdVccVenda()));
        jFmtData.setText(Util.dateToStr((Date) vcc_venda.getVccDataVenda()));
        jTxtTotal.setText(Util.doubleToStr(vcc_venda.getVccTotalVenda()));
        jCboCliente.setSelectedItem(vcc_venda.getVccCliente());
        jCboFornecedor.setSelectedItem(vcc_venda.getVccFornecedor());
    }

